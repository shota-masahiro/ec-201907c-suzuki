function sample(target) {
	var creditDiv = document.getElementById();
	
	if (target.value == "1") {
		creditDiv.style.display = "none";
	} else if (target.value == "2") {
		creditDiv.style.display = "block";
	} else if (target.value == "3") {
		creditDiv.style.display = "none";
	} else {
		creditDiv.style.display = "none";
	}

}
